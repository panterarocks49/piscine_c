#include <stdio.h>

void	ft_swap(int **a, int **b)
{
	int *tmp;

	*tmp = **a;
	**a = **b;
	**b = *tmp;
}

int		main(void)
{
	int *a; 
	*a = 42;
	int *b; 
	*b = 24;

	printf("%d\n%d\n", *a, *b);
	ft_swap(&a, &b);
	printf("%d\n%d\n", *a, *b);
}
