char	*ft_strlowcase(char *str);

char	*ft_strlowcase(char *str)
{
	char *s;

	s = str;
	while (*str != '\0')
	{
		if (*str >= 'A' && *str <= 'Z')
			*str += 'a' - 'A';
		str++;
	}
	return (s);
}
