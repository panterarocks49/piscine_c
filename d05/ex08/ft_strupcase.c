char	*ft_strupcase(char *str);

char	*ft_strupcase(char *str)
{
	char *s;

	s = str;
	while (*str != '\0')
	{
		if (*str >= 'a' && *str <= 'z')
			*str -= 'a' - 'A';
		str++;
	}
	return (s);
}
