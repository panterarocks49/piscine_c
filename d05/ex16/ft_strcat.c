char	*ft_strcat(char *dest, char *src);

char	*ft_strcat(char *dest, char *src)
{
	char *d;

	d = dest;
	while (*dest)
		dest++;
	while (*src)
	{
		*dest = *src;
		dest++;
		src++;
	}
	*dest = '\0';
	return (d);
}
