#include <unistd.h>

void	err_putchar(char c)
{
	write(2, &c, 1);
}

void	err_putstr(char *str)
{
	int i;

	i = 0;
	while (*(str + i) != '\0')
	{
		err_putchar(*(str + i));
		i++;
	}
}
